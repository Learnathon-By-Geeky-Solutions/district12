package com.district12.backend.enums;

public enum TaskType {
    WATER_PLANTS,      // Water the crops/plants.
    APPLY_FERTILIZER,  // Apply fertilizer to the crops.
    HARVEST_CROPS,     // Harvest mature crops.
    PEST_CONTROL,      // Apply pesticides or pest solutions.
    SOIL_TREATMENT,    // Improve soil quality (e.g., pH balancing).
    PRUNING,           // Trim plants to improve growth.
    IRRIGATION_CHECK,  // Inspect and maintain irrigation systems.
    SEED_PLANTING,     // Plant new seeds.
    WEED_REMOVAL,      // Remove weeds affecting crops.
    MONITOR_DISEASE,   // Check crops for potential diseases.
    FIELD_INSPECTION   // Perform a general field inspection.
}